    private void txt_articleKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_articleKeyReleased
        String content = txt_article.getText().toLowerCase();
        DefaultListModel list_model;
        if(!content.isEmpty()){
            list_articles = ArticuloDAO.buscarPorNombre(content);
             list_model = new DefaultListModel();
            for (Articulo article : list_articles) {
                list_model.addElement(article.getNombre());
            }
            
        } else {
             list_model = new DefaultListModel();
             this.cleanInputs();
        }
        list_article.setModel(list_model);
    }//GEN-LAST:event_txt_articleKeyReleased
    public void cleanInputs(){
        txt_articleCode.setText(null);
        txt_articleName.setText(null);
        txt_articleDescription.setText(null);
        txt_articlePrice.setText(null);
    }
    private void list_articleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_articleMouseClicked
        String list_selected = list_article.getSelectedValue();
        if(list_selected != null){
            for (Articulo article : list_articles) {
                if(article.getNombre().equals(list_selected)){
                    article_selected = new Articulo();
                    article_selected.setIdart(article.getIdart());
                    article_selected.setNombre(article.getNombre());
                    article_selected.setDescripcion(article.getDescripcion());
                    article_selected.setFoto(article.getFoto());
                    article_selected.setPrecio(article.getPrecio());
                }
            }
            txt_articleCode.setText(String.valueOf(article_selected.getIdart()));
            txt_articleName.setText(article_selected.getNombre());
            txt_articleDescription.setText(article_selected.getDescripcion());
            txt_articlePrice.setText(String.valueOf(article_selected.getPrecio()));
            
        }
    }//GEN-LAST:event_list_articleMouseClicked
    public void totalPrice(){
       double a = 0;
       for (int i = 0; i < tbl_articles.getRowCount(); i++) {
           a += Double.parseDouble(tbl_articles.getValueAt(i , 4).toString());
       } 
       txt_totalPrice.setText(String.valueOf(a));
    }

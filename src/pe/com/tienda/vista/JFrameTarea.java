    private void txt_clientNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_clientNameKeyReleased
        int key = evt.getKeyCode();
        if(key == KeyEvent.VK_ENTER){
            String name = txt_clientName.getText().toLowerCase();
            int id = Integer.parseInt(txt_clientID.getText());
            client_selected = new Cliente();
            client_selected = ClienteDAO.getClientByNameAndId(name, id);
            if(client_selected != null){
                txt_allName.setText(client_selected.getNombres());
            } else {
                txt_allName.setText("");
            }
            
        }
    }//GEN-LAST:event_txt_clientNameKeyReleased

